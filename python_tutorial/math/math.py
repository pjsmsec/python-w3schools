# Built-in math functions
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

print()

x = abs(-7.25)
print(x)

print()

x = pow(4, 3)
print(x)

# The math module
import math

x = math.sqrt(64)
print(x)

print()

# The math.ceil() method rounds a number upwards to it's nearest integer.
# The math.floor() method rounds a number downwards to it's nearest integer.
x = math.ceil(1.4)
y = math.floor(1.4)

print(x)
print(y)

print()

x = math.pi

print(x)
