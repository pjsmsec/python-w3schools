# F-strings
age = 36
txt = f"My name is John, I am {age}"

print(txt)

print()

# Placeholders and modifiers
price = 59
txt = f"The price is {price} dollars"

print(txt)

print()

# Modifiers
txt = f"The price is {price:.2f} dollars"

print(txt)

print()

# A placeholder can contain code
txt = f"The price is {20 * 59} dollars"

print(txt)
