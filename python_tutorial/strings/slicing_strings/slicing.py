b = "Hello, World!"
print(b[2:5])
# Arrays are 0 based
# The first value is inclusive and the last is exclusive

print()

# Slice from the start
print(b[:5])

print()

# Slice to the end
print(b[2:])

print()

# Negative indexing
print(b[-5:-2])
# The first value is inclusive and the last is exclusive
